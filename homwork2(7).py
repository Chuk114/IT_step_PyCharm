
baly = int(input("Введи кількість балів, отриманих на екзамені: "))

if 0 <= baly <= 49:
    print("Незадовільно.")
elif 50 <= baly <= 69:
    print("Задовільно.")
elif 70 <= baly <= 89:
    print("Добре.")
elif 90 <= baly <= 100:
    print("Відмінно.")
else:
    print("Помилка: кількість балів має бути від 0 до 100.")
